package com.pointlion.sys.mvc.common.model;
/**
 * 
 * @author Snake
 * 自定义的表单值类
 */
public class CustomValue {

	private String name;
	private String value;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
